#!/usr/bin/env python3
"""Run a REAL multi-sentence, multi-engine TTS benchmark and emit an evaluation manifest.

Sinh audio THAT cho mot bo cau tieng Viet da dang bang tat ca engine (F5-TTS, XTTS-v2,
MMS-TTS, Piper, Edge-TTS, Bark) tren CPU, do inference time tung cau, luu WAV va ghi
manifest JSON tuong thich voi scripts/evaluate.py. Ket qua duoc luu TANG DAN sau moi cau
va BO QUA file da co, nen co the chay lai/tiep tuc an toan.

Sau khi chay xong:
    python scripts/evaluate.py --manifest outputs/benchmark/manifest.json \
        --output-dir outputs/benchmark/evaluation
"""

from __future__ import annotations

import argparse
import json
import os
import sys
import time
from pathlib import Path

os.environ.setdefault("VVCS_DEVICE", "cpu")  # CPU: F5-TTS quality is validated on CPU

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))

import soundfile as sf  # noqa: E402

import app  # noqa: E402

REF_AUDIO = ROOT / "reference_audio" / "sample_clean_vi.wav"
OUT_DIR = ROOT / "outputs" / "benchmark"
MANIFEST = OUT_DIR / "manifest.json"

# Bo cau test da dang: ngan, so, ngay, cau hoi, cau ghep, cau dai.
TEST_SENTENCES: list[tuple[str, str]] = [
    ("s1-greeting", "Xin chào, đây là hệ thống nhân bản giọng nói tiếng Việt."),
    ("s2-general", "Trí tuệ nhân tạo đang thay đổi cách con người giao tiếp và làm việc mỗi ngày."),
    ("s3-number", "Hóa đơn có giá một trăm hai mươi lăm nghìn đồng."),
    ("s4-date", "Hôm nay là ngày mười tám tháng bảy năm hai nghìn không trăm hai mươi sáu."),
    ("s5-question", "Nếu trời mưa, chúng ta có nên hoãn chuyến đi không?"),
    ("s6-compound", "Mặc dù trời mưa rất to, các sinh viên vẫn đến lớp đúng giờ, vì hôm nay có bài kiểm tra quan trọng."),
    ("s7-long", "Trí tuệ nhân tạo đang hỗ trợ con người trong giáo dục, y tế và sản xuất; tuy nhiên, chúng ta cần sử dụng công nghệ một cách minh bạch, an toàn và có trách nhiệm."),
]

# Thu tu engine: cloning truoc, fixed-voice sau, bark (floor, cham) cuoi.
DEFAULT_ENGINES = ["base", "xtts", "mms", "piper", "edge", "bark"]


def load_cases() -> list[dict]:
    if MANIFEST.is_file():
        try:
            return json.loads(MANIFEST.read_text(encoding="utf-8")).get("cases", [])
        except (OSError, json.JSONDecodeError):
            return []
    return []


def save_manifest(cases: list[dict]) -> None:
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    MANIFEST.write_text(
        json.dumps({"schema_version": 1,
                    "notes": "Real multi-sentence benchmark generated by scripts/run_benchmark.py "
                             "(CPU, seed=42, NFE=32). Inference time measured per sentence.",
                    "cases": cases}, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )


def main() -> int:
    parser = argparse.ArgumentParser(description="Run real multi-sentence TTS benchmark")
    parser.add_argument("--engines", nargs="*", default=DEFAULT_ENGINES,
                        help=f"Registry keys; default: {', '.join(DEFAULT_ENGINES)}")
    parser.add_argument("--seed", type=int, default=app.DEFAULT_SEED)
    parser.add_argument("--nfe", type=int, default=32)
    parser.add_argument("--speed", type=float, default=1.0)
    args = parser.parse_args()

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    if not REF_AUDIO.is_file():
        print(f"Missing reference audio: {REF_AUDIO}", file=sys.stderr)
        return 2

    # Transcript cua audio mau (mot lan), giong app.
    ref_text = app._normalize_vi(app._transcribe_whisper(str(REF_AUDIO)), add_end_punct=False)
    print(f"Device={app.DEVICE} · seed={args.seed} · NFE={args.nfe}")
    print(f"Reference transcript: {ref_text!r}\n")

    cases = load_cases()
    done = {(c["engine"], c["id"]) for c in cases}
    total = len(args.engines) * len(TEST_SENTENCES)
    step = 0
    for key in args.engines:
        if key not in app.MODEL_REGISTRY:
            print(f"skip unknown engine '{key}'")
            continue
        engine = app.MODEL_REGISTRY[key]["engine"]
        needs_ref = engine in app.REF_REQUIRED_ENGINES
        for sid, text in TEST_SENTENCES:
            step += 1
            case_id = f"{engine}-{sid}"
            wav_path = OUT_DIR / f"{engine}__{sid}.wav"
            if (engine, case_id) in done and wav_path.is_file():
                print(f"[{step}/{total}] skip existing {case_id}")
                continue
            gen_text = app._normalize_vi(text, add_end_punct=True)
            print(f"[{step}/{total}] {engine:6s} · {sid} ...", flush=True)
            t0 = time.perf_counter()
            try:
                wave, sr = app._infer_one(key, str(REF_AUDIO), ref_text, gen_text,
                                          args.speed, args.nfe, seed=args.seed)
                dt = time.perf_counter() - t0
                sf.write(str(wav_path), wave, sr)
                case = {
                    "id": case_id, "reference_text": text,
                    "generated_audio": str(wav_path), "engine": engine,
                    "seed": args.seed, "nfe": args.nfe if engine == "f5tts" else None,
                    "inference_time": round(dt, 2),
                }
                if needs_ref:
                    case["reference_audio"] = str(REF_AUDIO)
                print(f"        ok  {dt:6.1f}s → {wav_path.name}")
            except Exception as exc:  # per-cell failure never aborts the batch
                dt = time.perf_counter() - t0
                case = {"id": case_id, "reference_text": text,
                        "generated_audio": str(wav_path), "engine": engine,
                        "seed": args.seed, "nfe": None, "inference_time": None,
                        "error": f"{type(exc).__name__}: {exc}"}
                print(f"        FAIL {type(exc).__name__}: {exc}")
            cases = [c for c in cases if c["id"] != case_id] + [case]
            save_manifest(cases)

    ok = sum(1 for c in cases if "error" not in c)
    print(f"\nGenerated {ok}/{len(cases)} cases. Manifest: {MANIFEST}")
    return 0 if ok else 1


if __name__ == "__main__":
    raise SystemExit(main())
